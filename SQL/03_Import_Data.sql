# Step 1
Open MySQL Workbench.

# Step 2

On the left side under SCHEMAS, locate:
customer_churn_db

# Step 3

Right-click on Tables.
Select:
Table Data Import Wizard

# Step 4

Browse to your dataset:
Telco-Customer-Churn.csv
Click Next.

# Step 5

Choose:
✅ Use existing table
Select:
customer_churn
Click Next.

# Step 6

Workbench will display all the columns.
Click Next.

# Step 7

Click Next again.
The import will begin
Click Finish.